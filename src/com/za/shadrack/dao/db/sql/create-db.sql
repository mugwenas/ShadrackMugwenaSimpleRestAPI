CREATE TABLE USERS (ID NUMBER NOT NULL, USERNAME VARCHAR2(100) NOT NULL, PASSWORD VARCHAR2(100) NOT NULL, PHONE VARCHAR2(100) NOT NULL);
CREATE TABLE USER_TOKENS (ID NUMBER NOT NULL, TOKEN VARCHAR2(150) NOT NULL, EXPIRY_DATE TIMESTAMP, USER_ID NUMBER NOT NULL);

CREATE SEQUENCE USERS_SEQ AS INTEGER START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE USER_TOKENS_SEQ AS INTEGER START WITH 1 INCREMENT BY 1;

